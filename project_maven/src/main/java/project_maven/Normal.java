package project_maven;

public class Normal extends Calculatrice{

	public static int add(int a, int b) {
		return (a+b);
	}
	
	public static int minus(int a, int b) {
		return (a-b);
	}
	
	public static int mult(int a, int b) {
		return (a*b);
	}
	
	public static int div(int a, int b) {
		return (int)(a/b);
	}
	
}